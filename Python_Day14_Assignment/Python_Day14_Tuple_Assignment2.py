#2.Write a Python program to convert a list to a tuple.

#Input: listx = [5, 10, 7, 4, 15, 3] Output: (5, 10, 7, 4, 15, 3)

# Define the list
listx = [5, 10, 7, 4, 15, 3]

# Convert the list to a tuple
tuplex = tuple(listx)

# Print the result
print(tuplex)

#output: (5, 10, 7, 4, 15, 3)
